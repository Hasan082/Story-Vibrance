import com.facebook.shimmer.ShimmerFrameLayout;

public class ShimmerEffectUtil {

    public static void startShimmerEffect(ShimmerFrameLayout shimmerFrameLayout) {
        shimmerFrameLayout.startShimmer();
    }

    public static void stopShimmerEffect(ShimmerFrameLayout shimmerFrameLayout) {
        shimmerFrameLayout.stopShimmer();
    }
}
